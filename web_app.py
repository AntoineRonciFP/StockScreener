def build_search_index(df: pd.DataFrame) -> pd.Series:
    if df.empty:
        return pd.Series(dtype=str)
    candidate_cols = [
        "ticker",
        "company_name",
        "sector",
        "country",
        "currency",
        "company_description",
        "Upgrades / downgrades",
        "Analyst recommendations",
    ]
    cols = [col for col in candidate_cols if col in df.columns]
    if not cols:
        return pd.Series([""] * len(df), index=df.index)
    return df[cols].fillna("").astype(str).agg(" ".join, axis=1).str.lower()


def parse_float_input(value: Any) -> Optional[float]:
    if value is None or value == "":
        return None
    try:
        return float(str(value).replace(",", "."))
    except ValueError:
        return None


def apply_numeric_range(df: pd.DataFrame, column: str, min_value: Optional[float], max_value: Optional[float]) -> pd.DataFrame:
    if column not in df.columns or (min_value is None and max_value is None):
        return df
    values = pd.to_numeric(df[column], errors="coerce")
    mask = pd.Series(True, index=df.index)
    if min_value is not None:
        mask &= values >= min_value
    if max_value is not None:
        mask &= values <= max_value
    return df[mask]


def apply_market_cap_bucket(df: pd.DataFrame, bucket: str) -> pd.DataFrame:
    """
    Filtre par bucket de capitalisation boursiere.
    Les seuils sont appliques directement sur la colonne market_cap renvoyee par Yahoo Finance.
    """
    if not bucket or bucket == "All" or "market_cap" not in df.columns:
        return df

    min_value, max_value = MARKET_CAP_BUCKETS.get(bucket, (None, None))
    values = pd.to_numeric(df["market_cap"], errors="coerce")
    mask = pd.Series(True, index=df.index)

    if min_value is not None:
        mask &= values > min_value if bucket.startswith("Mega Cap") else values >= min_value
    if max_value is not None:
        mask &= values < max_value if bucket.startswith("Small Cap") else values <= max_value

    return df[mask]


def apply_vol_strike_bucket(df: pd.DataFrame, bucket: str) -> pd.DataFrame:
    """
    Filtre par bucket de volatilite variance swap, en pourcentage.
    La colonne variance_swap_strike_vol_pct est deja exprimee en pourcentage.
    """
    if not bucket or bucket == "All" or "variance_swap_strike_vol_pct" not in df.columns:
        return df

    min_value, max_value = VOL_STRIKE_BUCKETS.get(bucket, (None, None))
    values = pd.to_numeric(df["variance_swap_strike_vol_pct"], errors="coerce")
    mask = pd.Series(True, index=df.index)

    if bucket == "Very High Vol > 60%":
        mask &= values > min_value
    elif bucket == "High Vol 40%-60%":
        mask &= (values >= min_value) & (values <= max_value)
    elif bucket == "Mid Vol 20%-40%":
        mask &= (values >= min_value) & (values < max_value)
    elif bucket == "Low Vol < 20%":
        mask &= values < max_value

    return df[mask]


def filter_dataframe(
    df: pd.DataFrame,
    search_text: str,
    sector: str,
    country: str,
    currency: str,
    market_cap_bucket: str,
    var_vol_bucket: str,
    pe_min: Optional[float],
    pe_max: Optional[float],
    ytd_min_pct: Optional[float],
    ytd_max_pct: Optional[float],
    one_week_min_pct: Optional[float],
    one_week_max_pct: Optional[float],
) -> pd.DataFrame:
    filtered = df.copy()

    search_text = (search_text or "").strip().lower()
    if search_text:
        index = build_search_index(filtered)
        filtered = filtered[index.str.contains(search_text, na=False, regex=False)]

    if sector and sector != "All" and "sector" in filtered.columns:
        filtered = filtered[filtered["sector"].fillna("").astype(str).str.strip() == sector]

    if country and country != "All" and "country" in filtered.columns:
        filtered = filtered[filtered["country"].fillna("").astype(str).str.strip() == country]

    if currency and currency != "All" and "currency" in filtered.columns:
        filtered = filtered[filtered["currency"].fillna("").astype(str).str.strip() == currency]

    filtered = apply_market_cap_bucket(filtered, market_cap_bucket)
    filtered = apply_vol_strike_bucket(filtered, var_vol_bucket)
    filtered = apply_numeric_range(filtered, "price_earnings_ratio", pe_min, pe_max)

    # Returns are stored as decimals in Excel. User inputs are percentages.
    filtered = apply_numeric_range(
        filtered,
        "YTD return",
        ytd_min_pct / 100 if ytd_min_pct is not None else None,
        ytd_max_pct / 100 if ytd_max_pct is not None else None,
    )
    filtered = apply_numeric_range(
        filtered,
        "1 week return",
        one_week_min_pct / 100 if one_week_min_pct is not None else None,
        one_week_max_pct / 100 if one_week_max_pct is not None else None,
    )

    return filtered



def prepare_table_dataframe(df: pd.DataFrame) -> pd.DataFrame:
    table_cols = [col for col in TABLE_COLUMNS if col in df.columns]
    display = df[table_cols].copy()

    # Convert return columns from decimals to percentages for display in the grid.
    for col in ["YTD return", "1 week return"]:
        if col in display.columns:
            display[col] = pd.to_numeric(display[col], errors="coerce") * 100

    if "dividend_yield" in display.columns:
        div = pd.to_numeric(display["dividend_yield"], errors="coerce")
        display["dividend_yield"] = div.where(div.abs() > 2, div * 100)

    if "variance_swap_strike_vol" in display.columns:
        vol = pd.to_numeric(display["variance_swap_strike_vol"], errors="coerce")
        display["variance_swap_strike_vol"] = vol.where(vol.abs() > 2, vol * 100)

    for col in display.columns:
        if col in DATE_COLUMNS or "date" in col.lower() or "expiration" in col.lower():
            display[col] = display[col].apply(format_date)

    return display


def build_column_config(df: pd.DataFrame) -> dict[str, Any]:
    config: dict[str, Any] = {}

    for col in df.columns:
        label = DISPLAY_NAMES.get(col, col)
        if col in {"YTD return", "1 week return", "dividend_yield", "variance_swap_strike_vol", "variance_swap_strike_vol_pct"}:
            config[col] = st.column_config.NumberColumn(label, format="%.2f%%")
        elif col == "market_cap":
            config[col] = st.column_config.NumberColumn(label, format="%.0f")
        elif col in NUMERIC_COLUMNS:
            config[col] = st.column_config.NumberColumn(label, format="%.2f")
        else:
            config[col] = st.column_config.TextColumn(label)

    return config


def get_selected_row_by_ticker(ticker: str, filtered_df: pd.DataFrame) -> Optional[pd.Series]:
    if not ticker or "ticker" not in filtered_df.columns:
        return None
    matches = filtered_df[filtered_df["ticker"].astype(str) == str(ticker)]
    if matches.empty:
        return None
    return matches.iloc[0]


def fallback_selected_row(filtered_df: pd.DataFrame) -> Optional[pd.Series]:
    if filtered_df.empty:
        return None
    saved_ticker = st.session_state.get("selected_ticker")
    if saved_ticker:
        matched = get_selected_row_by_ticker(str(saved_ticker), filtered_df)
        if matched is not None:
            return matched
    return filtered_df.iloc[0]


def reset_filters() -> None:
    for key in FILTER_STATE_KEYS:
        st.session_state.pop(key, None)


def run_extraction_script() -> tuple[int, str, str]:
    script_path = APP_DIR / DEFAULT_MAIN_SCRIPT
    if not script_path.exists():
        return 1, "", f"{DEFAULT_MAIN_SCRIPT} not found in {APP_DIR}"

    timeout_seconds = int(get_secret_or_env("REFRESH_TIMEOUT_SECONDS", 60 * 60))
    cmd = [sys.executable, str(script_path)]

    result = subprocess.run(
        cmd,
        cwd=str(APP_DIR),
        capture_output=True,
        text=True,
        timeout=timeout_seconds,
        check=False,
    )
    return result.returncode, result.stdout, result.stderr


def require_password_if_configured() -> None:
    password = get_secret_or_env("APP_PASSWORD", None)
    if not password:
        return

    if st.session_state.get("authenticated"):
        return

    st.title("Marex Solutions - Stock Screener")
    st.caption("Access protected")
    entered = st.text_input("Password", type="password")
    if st.button("Log in"):
        if entered == str(password):
            st.session_state["authenticated"] = True
            st.rerun()
        else:
            st.error("Invalid password")
    st.stop()


