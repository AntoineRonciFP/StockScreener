def subset_correlation_matrix(corr: pd.DataFrame, tickers: list[str]) -> pd.DataFrame:
    if corr is None or corr.empty:
        return pd.DataFrame()
    clean_tickers = []
    seen = set()
    for ticker in tickers:
        ticker = str(ticker).strip()
        if ticker and ticker not in seen:
            clean_tickers.append(ticker)
            seen.add(ticker)
    available = [ticker for ticker in clean_tickers if ticker in corr.index and ticker in corr.columns]
    if len(available) < 2:
        return pd.DataFrame()
    return corr.loc[available, available].astype(float)


def build_pairwise_correlation_table(corr_subset: pd.DataFrame) -> pd.DataFrame:
    """
    Transforme une matrice de correlation en tableau de toutes les paires uniques.

    Sortie :
    - Ticker 1
    - Ticker 2
    - Correlation

    La diagonale et les doublons symetriques sont exclus.
    """
    columns = ["Ticker 1", "Ticker 2", "Correlation"]
    if corr_subset is None or corr_subset.empty:
        return pd.DataFrame(columns=columns)

    tickers = [str(ticker).strip() for ticker in corr_subset.index if str(ticker).strip()]
    rows: list[dict[str, Any]] = []

    for i, ticker_1 in enumerate(tickers):
        for ticker_2 in tickers[i + 1:]:
            if ticker_1 not in corr_subset.index or ticker_2 not in corr_subset.columns:
                continue
            correlation = safe_float(corr_subset.loc[ticker_1, ticker_2])
            if correlation is None:
                continue
            rows.append({
                "Ticker 1": ticker_1,
                "Ticker 2": ticker_2,
                "Correlation": correlation,
            })

    if not rows:
        return pd.DataFrame(columns=columns)

    return (
        pd.DataFrame(rows, columns=columns)
        .sort_values(["Correlation", "Ticker 1", "Ticker 2"], ascending=[True, True, True])
        .reset_index(drop=True)
    )


