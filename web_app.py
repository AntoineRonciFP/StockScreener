def get_selected_row_from_streamlit_event(event: Any, filtered_df: pd.DataFrame) -> Optional[pd.Series]:
    if filtered_df.empty:
        return None

    selected_rows: list[int] = []
    try:
        selected_rows = list(event.selection.rows)
    except Exception:
        try:
            selected_rows = list(event.get("selection", {}).get("rows", []))
        except Exception:
            selected_rows = []

    if selected_rows:
        row_pos = selected_rows[0]
        if 0 <= row_pos < len(filtered_df):
            return filtered_df.iloc[row_pos]

    return fallback_selected_row(filtered_df)


def get_selected_row_from_aggrid(selected_rows: Any, filtered_df: pd.DataFrame) -> Optional[pd.Series]:
    if filtered_df.empty:
        return None

    selected_record: Optional[dict[str, Any]] = None
    if selected_rows is None:
        selected_record = None
    elif isinstance(selected_rows, pd.DataFrame):
        if not selected_rows.empty:
            selected_record = selected_rows.iloc[0].to_dict()
    elif isinstance(selected_rows, list):
        if selected_rows:
            first = selected_rows[0]
            if isinstance(first, dict):
                selected_record = first
    elif isinstance(selected_rows, dict):
        selected_record = selected_rows

    if selected_record:
        ticker = normalize_text(selected_record.get("ticker"))
        matched = get_selected_row_by_ticker(ticker, filtered_df)
        if matched is not None:
            return matched

    return fallback_selected_row(filtered_df)


