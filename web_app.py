def render_toolbar(uploaded_excel_key: str) -> Optional[Any]:
    toolbar_cols = st.columns([1.0, 1.35, 1.2, 3.0], gap="small")
    with toolbar_cols[0]:
        if st.button("Reload Excel", use_container_width=True):
            st.cache_data.clear()
            st.rerun()
    with toolbar_cols[1]:
        enable_refresh = to_bool(get_secret_or_env("ENABLE_REFRESH", False), default=False)
        if enable_refresh:
            if st.button("Refresh Yahoo Finance", use_container_width=True):
                refresh_password = get_secret_or_env("REFRESH_PASSWORD", None)
                entered_refresh_password = st.session_state.get("refresh_password", "")
                if refresh_password and entered_refresh_password != str(refresh_password):
                    st.error("Invalid refresh password")
                else:
                    with st.spinner("Running main.py. This can take several minutes..."):
                        try:
                            code, out, err = run_extraction_script()
                            st.cache_data.clear()
                            if code == 0:
                                st.success("Extraction completed. Data reloaded.")
                                if out:
                                    st.text_area("Extraction output", out, height=180)
                                st.rerun()
                            else:
                                st.error(f"Extraction failed with exit code {code}")
                                if out:
                                    st.text_area("stdout", out, height=160)
                                if err:
                                    st.text_area("stderr", err, height=160)
                        except subprocess.TimeoutExpired:
                            st.error("Extraction timeout reached.")
                        except Exception as exc:
                            st.error(f"Extraction error: {exc}")
        else:
            st.button("Refresh disabled", use_container_width=True, disabled=True)
    with toolbar_cols[2]:
        if to_bool(get_secret_or_env("ENABLE_REFRESH", False), default=False) and get_secret_or_env("REFRESH_PASSWORD", None):
            st.text_input("Refresh password", type="password", key="refresh_password", label_visibility="collapsed", placeholder="Refresh password")
    with toolbar_cols[3]:
        uploaded_excel = st.file_uploader(
            "Upload Excel for this session",
            type=["xlsx", "xlsm", "xls"],
            key=uploaded_excel_key,
            label_visibility="collapsed",
        )
    return uploaded_excel

