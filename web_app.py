def is_null(value: Any) -> bool:
    if value is None:
        return True
    try:
        return bool(pd.isna(value))
    except Exception:
        return False


def safe_float(value: Any) -> Optional[float]:
    if is_null(value):
        return None
    if isinstance(value, (int, float, np.integer, np.floating)):
        val = float(value)
        return val if math.isfinite(val) else None
    if isinstance(value, str):
        cleaned = (
            value.strip()
            .replace("%", "")
            .replace(",", "")
            .replace(" ", "")
            .replace("\u00a0", "")
        )
        if not cleaned:
            return None
        try:
            val = float(cleaned)
            return val if math.isfinite(val) else None
        except ValueError:
            return None
    return None


def normalize_text(value: Any) -> str:
    if is_null(value):
        return ""
    return str(value).strip()


def escape(value: Any) -> str:
    return html.escape(normalize_text(value))


def human_number(value: Any, decimals: int = 1) -> str:
    val = safe_float(value)
    if val is None:
        return ""
    abs_val = abs(val)
    if abs_val >= 1_000_000_000_000:
        return f"{val / 1_000_000_000_000:,.{decimals}f}T"
    if abs_val >= 1_000_000_000:
        return f"{val / 1_000_000_000:,.{decimals}f}B"
    if abs_val >= 1_000_000:
        return f"{val / 1_000_000:,.{decimals}f}M"
    if abs_val >= 1_000:
        return f"{val / 1_000:,.{decimals}f}K"
    return f"{val:,.{decimals}f}"


def format_date(value: Any) -> str:
    if is_null(value):
        return ""
    try:
        ts = pd.to_datetime(value, errors="coerce")
    except Exception:
        return str(value)
    if pd.isna(ts):
        return str(value)
    if getattr(ts, "tzinfo", None) is not None:
        try:
            ts = ts.tz_convert(None)
        except Exception:
            ts = ts.tz_localize(None)
    if getattr(ts, "hour", 0) == 0 and getattr(ts, "minute", 0) == 0 and getattr(ts, "second", 0) == 0:
        return ts.strftime("%Y-%m-%d")
    return ts.strftime("%Y-%m-%d %H:%M")


def format_percent(value: Any, column: str) -> str:
    val = safe_float(value)
    if val is None:
        return ""
    if column in {"YTD return", "1 week return"}:
        # Stock returns are stored as decimals in Excel: 0.05 = 5%.
        val *= 100
    elif column in {"dividend_yield", "variance_swap_strike_vol"}:
        if abs(val) <= 2:
            val *= 100
    return f"{val:,.2f}%"


def format_cell_value(column: str, value: Any) -> str:
    if is_null(value):
        return ""
    if column in DATE_COLUMNS or "date" in column.lower() or "expiration" in column.lower():
        return format_date(value)
    if column == "market_cap":
        return human_number(value)
    if column in {"dividend_yield", "YTD return", "1 week return", "variance_swap_strike_vol", "variance_swap_strike_vol_pct"}:
        return format_percent(value, column)
    if column in NUMERIC_COLUMNS:
        val = safe_float(value)
        if val is None:
            return ""
        if abs(val) >= 1000:
            return f"{val:,.0f}"
        return f"{val:,.2f}"
    return str(value)


def reorder_columns(df: pd.DataFrame) -> pd.DataFrame:
    existing = [col for col in PREFERRED_COLUMNS if col in df.columns]
    remaining = [col for col in df.columns if col not in existing]
    return df[existing + remaining]


def clean_dataframe(df: pd.DataFrame) -> pd.DataFrame:
    df = df.copy()
    df.columns = [str(c).strip() for c in df.columns]

    for col in df.columns:
        if col in NUMERIC_COLUMNS:
            df[col] = pd.to_numeric(df[col], errors="coerce")
        elif col in DATE_COLUMNS or "date" in col.lower() or "expiration" in col.lower():
            df[col] = pd.to_datetime(df[col], errors="coerce")
            try:
                if getattr(df[col].dt, "tz", None) is not None:
                    df[col] = df[col].dt.tz_localize(None)
            except Exception:
                pass

    return reorder_columns(df)


def get_secret_or_env(name: str, default: Any = None) -> Any:
    """Read from Streamlit secrets first, then environment variables."""
    for key in (name, name.lower(), name.upper()):
        try:
            if key in st.secrets:
                return st.secrets[key]
        except Exception:
            pass
        if key in os.environ:
            return os.environ[key]
    return default


def to_bool(value: Any, default: bool = False) -> bool:
    if value is None:
        return default
    if isinstance(value, bool):
        return value
    return str(value).strip().lower() in {"1", "true", "yes", "y", "on"}


