def render_detail_panel(row: Optional[pd.Series]) -> None:
    if row is None:
        st.markdown(
            """
            <div class='right-card'>
                <div class='detail-title'>No ticker selected</div>
                <div class='detail-subtitle'>Click a row in the table to display ticker details.</div>
            </div>
            """,
            unsafe_allow_html=True,
        )
        return

    ticker = normalize_text(row.get("ticker"))
    company = normalize_text(row.get("company_name"))
    sector = normalize_text(row.get("sector"))
    country = normalize_text(row.get("country"))
    currency = normalize_text(row.get("currency"))
    subtitle = " | ".join([x for x in [company, sector, country, currency] if x])
    description = normalize_text(row.get("company_description")) or "No description available."

    details_html: list[str] = [
        "<div class='right-card'>",
        f"<div class='detail-title'>{escape(ticker or 'Ticker')}</div>",
    ]
    if subtitle:
        details_html.append(f"<div class='detail-subtitle'>{escape(subtitle)}</div>")

    details_html.extend(
        [
            "<div class='detail-section-title'>Company description</div>",
            f"<div class='description-box'>{escape(description)}</div>",
            "<div class='detail-section-title'>Ticker details</div>",
        ]
    )

    for field in DETAIL_FIELDS:
        if field not in row.index:
            continue
        label = DISPLAY_NAMES.get(field, field)
        value = format_cell_value(field, row.get(field)) or "-"
        value_class = "detail-value"
        if field in {"YTD return", "1 week return"}:
            raw_val = safe_float(row.get(field))
            if raw_val is not None and raw_val > 0:
                value_class += " positive"
            elif raw_val is not None and raw_val < 0:
                value_class += " negative"
        details_html.append(
            "<div class='detail-row'>"
            f"<div class='detail-key'>{escape(label)}</div>"
            f"<div class='{value_class}'>{escape(value)}</div>"
            "</div>"
        )

    details_html.append("</div>")
    st.markdown("\n".join(details_html), unsafe_allow_html=True)


