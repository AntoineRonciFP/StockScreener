def _selected_rows_to_records(selected_rows: Any) -> list[dict[str, Any]]:
    if selected_rows is None:
        return []
    if isinstance(selected_rows, pd.DataFrame):
        return selected_rows.to_dict("records")
    if isinstance(selected_rows, list):
        return [row for row in selected_rows if isinstance(row, dict)]
    if isinstance(selected_rows, dict):
        return [selected_rows]
    return []


def build_aggrid_update_mode():
    """
    Keep checkbox selection updates and also listen to the hidden field update
    written by the row double-click JavaScript handler.
    """
    mode = GridUpdateMode.SELECTION_CHANGED
    for attr_name in ("VALUE_CHANGED", "MODEL_CHANGED"):
        try:
            mode = mode | getattr(GridUpdateMode, attr_name)
        except Exception:
            pass
    return mode


def get_detail_ticker_from_aggrid_response(response: dict[str, Any]) -> Optional[str]:
    """
    Return a ticker only when a real row double-click occurred.

    Checkbox selection changes and filter changes may rerun the app, but they do
    not write the hidden _detail_event_id field. This prevents ticker details
    from opening just because a checkbox was ticked.
    """
    if not response:
        return None

    records: list[dict[str, Any]] = []
    records.extend(_selected_rows_to_records(response.get("selected_rows")))
    records.extend(_selected_rows_to_records(response.get("data")))

    last_seen = safe_float(st.session_state.get("last_detail_event_id")) or 0.0
    best_event_id = last_seen
    best_ticker: Optional[str] = None

    for row in records:
        event_id = safe_float(row.get("_detail_event_id"))
        ticker = normalize_text(row.get("_detail_ticker")) or normalize_text(row.get("ticker"))
        if event_id is not None and event_id > best_event_id and ticker:
            best_event_id = event_id
            best_ticker = ticker

    if best_ticker is not None:
        st.session_state["last_detail_event_id"] = best_event_id

    return best_ticker


def get_selected_tickers_from_aggrid(selected_rows: Any) -> list[str]:
    records = _selected_rows_to_records(selected_rows)
    tickers = []
    seen = set()
    for row in records:
        ticker = normalize_text(row.get("ticker"))
        if ticker and ticker not in seen:
            tickers.append(ticker)
            seen.add(ticker)
    return tickers


def get_detail_row_from_selected_tickers(selected_tickers: list[str], filtered_df: pd.DataFrame) -> Optional[pd.Series]:
    # A single selected stock opens the ticker detail dialog. Multiple selected stocks
    # are interpreted as a correlation basket.
    if len(selected_tickers) != 1:
        return None
    return get_selected_row_by_ticker(selected_tickers[0], filtered_df)


def render_selectable_table(table_df: pd.DataFrame, filtered_df: pd.DataFrame) -> tuple[list[str], Optional[str]]:
    if table_df.empty:
        st.warning("No rows match the current filters.")
        return [], None

    selected_tickers: list[str] = []
    detail_ticker_to_open: Optional[str] = None

    if AGGRID_AVAILABLE:
        grid_df = table_df.copy()
        grid_df["_detail_event_id"] = 0
        grid_df["_detail_ticker"] = ""

        gb = GridOptionsBuilder.from_dataframe(grid_df)
        gb.configure_default_column(sortable=True, filter=True, resizable=True, min_column_width=92)
        gb.configure_selection("multiple", use_checkbox=True)
        gb.configure_grid_options(
            domLayout="normal",
            rowHeight=34,
            headerHeight=38,
            suppressRowHoverHighlight=False,
            animateRows=False,
            enableCellTextSelection=True,
            ensureDomOrder=True,
            rowMultiSelectWithClick=False,
            suppressRowClickSelection=True,
            onRowDoubleClicked=DETAIL_DOUBLE_CLICK_JS,
        )

        for col in grid_df.columns:
            if col in {"_detail_event_id", "_detail_ticker"}:
                gb.configure_column(col, hide=True, editable=True)
                continue

            header = DISPLAY_NAMES.get(col, col)
            width = 120
            if col == "ticker":
                width = 105
                gb.configure_column(col, header_name=header, pinned="left", width=width, checkboxSelection=True, headerCheckboxSelection=True)
            elif col == "company_name":
                width = 255
                gb.configure_column(col, header_name=header, width=width)
            elif col == "sector":
                width = 170
                gb.configure_column(col, header_name=header, width=width)
            elif col == "country":
                width = 135
                gb.configure_column(col, header_name=header, width=width)
            elif col in {"YTD return", "1 week return", "dividend_yield", "variance_swap_strike_vol", "variance_swap_strike_vol_pct", "variance_swap_strike_vol_pct_180d", "variance_swap_strike_vol_pct_360d"}:
                width = 125
                gb.configure_column(
                    col,
                    header_name=header,
                    width=width,
                    type=["numericColumn"],
                    valueFormatter=PERCENT_VALUE_FORMATTER,
                )
            elif col in NUMERIC_COLUMNS:
                width = 125
                gb.configure_column(col, header_name=header, width=width, type=["numericColumn"])
            else:
                gb.configure_column(col, header_name=header, width=width)

        response = AgGrid(
            grid_df,
            gridOptions=gb.build(),
            height=640,
            width="100%",
            data_return_mode=DataReturnMode.FILTERED_AND_SORTED,
            update_mode=build_aggrid_update_mode(),
            fit_columns_on_grid_load=False,
            theme="alpine",
            custom_css=AGGRID_CSS,
            allow_unsafe_jscode=True,
            key="marex_universe_aggrid",
        )
        selected_tickers = get_selected_tickers_from_aggrid(response.get("selected_rows"))
        detail_ticker_to_open = get_detail_ticker_from_aggrid_response(response)
    else:
        event = st.dataframe(
            table_df,
            hide_index=True,
            use_container_width=True,
            height=640,
            column_config=build_column_config(table_df),
            on_select="rerun",
            selection_mode="multi-row",
            key="marex_universe_table",
        )
        selected_rows = []
        try:
            selected_rows = list(event.selection.rows)
        except Exception:
            try:
                selected_rows = list(event.get("selection", {}).get("rows", []))
            except Exception:
                selected_rows = []
        for row_pos in selected_rows:
            if 0 <= row_pos < len(filtered_df):
                ticker = normalize_text(filtered_df.iloc[row_pos].get("ticker"))
                if ticker:
                    selected_tickers.append(ticker)

    return selected_tickers, detail_ticker_to_open


